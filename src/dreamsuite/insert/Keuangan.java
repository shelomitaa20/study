       public void insert(){
        String nolaporan = jTextFieldNo.getText();
        String pendapatankotor = jTextFieldKotor.getText();
        String pendapatanbersih = jTextFieldBersih.getText();
        try{
            Connection kon = getKoneksi();
            Statement s = kon.createStatement();
            String query = "INSERT INTO keuangan VALUES('"+nolaporan+"', '"+pendapatankotor+"', '"+pendapatanbersih+"')";
            int r = s.executeUpdate(query);
        } catch (SQLException ex){
            System.out.println(ex.getMessage());
        }
    }

