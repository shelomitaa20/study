        String nolaporan = jTextFieldNo.getText();
        String jumlahpemesanankamar = jTextFieldJumlah.getText();
        try{
            Connection kon = getKoneksi();
            Statement s = kon.createStatement();
            String query = "INSERT INTO administrasi VALUES('"+nolaporan+"', '"+jumlahpemesanankamar+"')";
            int r = s.executeUpdate(query);
        } catch (SQLException ex){
            System.out.println(ex.getMessage());
        }
